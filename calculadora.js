function somar(numUm, numDois){
    return numUm + numDois;
}

function dividir(numUm, numDois){
    return numUm / numDois;
}

function multiplicar(numUm, numDois){
    return numUm * numDois;
}

function subtrair(numUm, numDois){
    return numUm - numDois;
}

module.exports = {somar, subtrair, multiplicar, dividir}